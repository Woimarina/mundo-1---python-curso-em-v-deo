n = input('digita qualquer coisa ai e vamo testar o que diabo é: ')

print('o tipo primitivo desse valor é? ',type(n))
print('esse valor é um alfanumerico? ', n.isalnum())
print('esse valor é uma letra? ', n.isalpha())
print('esse valor é um numero? ', n.isnumeric())
print('esse valor está todo em maiusculas: ', n.isupper())
print('esse valor está todo em minusculas? ', n.islower())
print('esse valor pode ser printado na tela? ', n.isprintable())

